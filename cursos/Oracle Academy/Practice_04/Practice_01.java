package Practice_04;

public class Practice_01 {
	public static void main(String[] args) {
		
		//primera forma de declarar e iniciar un string
		String abc = "abc";
		System.out.println(abc);
		
		//segunda forma de declarar e iniciar un string
		String bcd;
		bcd = "bcd";
		System.out.println(bcd);
		
		System.out.println(abc.concat(bcd));
	}
}
